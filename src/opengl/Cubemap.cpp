Cubemap::Cubemap(const std::array<std::string, 6>& facesFilePaths)
{
    glGenTextures(1, &m_Id);
    glBindTexture(GL_TEXTURE_CUBE_MAP, m_Id);

    int width, height, nrComponents;
    for (unsigned int i = 0; i < facesFilePaths.size(); i++)
    {
        unsigned char* data = stbi_load(facesFilePaths[i].c_str(), &width, &height, &nrComponents, 0);
        if (!data)
        {
            logger::error("Cubemap texture failed to load at path: {}", facesFilePaths[i]);
            continue;
        }
        glTexImage2D(GL_TEXTURE_CUBE_MAP_POSITIVE_X + i, 0, GL_RGBA, width, height, 0, GL_RGBA, GL_UNSIGNED_BYTE, data);
        stbi_image_free(data);
    }
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MIN_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_MAG_FILTER, GL_LINEAR);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_S, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_T, GL_CLAMP_TO_EDGE);
    glTexParameteri(GL_TEXTURE_CUBE_MAP, GL_TEXTURE_WRAP_R, GL_CLAMP_TO_EDGE);

    CHECK_GL(glBindTexture(GL_TEXTURE_CUBE_MAP, 0));

    glGenVertexArrays(1, &m_VAO);
    glBindVertexArray(m_VAO);
    glGenBuffers(1, &m_VBO);
    glBindBuffer(GL_ARRAY_BUFFER, m_VBO);
    glBufferData(GL_ARRAY_BUFFER, sizeof(skyboxVertices), &skyboxVertices, GL_STATIC_DRAW);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 3 * sizeof(float), nullptr);

    glBindBuffer(GL_ARRAY_BUFFER, 0);
    glBindVertexArray(0);
}

Cubemap::~Cubemap()
{
    glDeleteVertexArrays(1, &m_VAO);
    glDeleteBuffers(1, &m_VBO);
	CHECK_GL(glDeleteTextures(1, &m_Id));
}

void Cubemap::bind(unsigned int slot) const
{
    glBindVertexArray(m_VAO);
    CHECK_GL(glActiveTexture(GL_TEXTURE0 + slot));
    glBindTexture(GL_TEXTURE_CUBE_MAP, m_Id);
}

void Cubemap::unbind() const
{
    glBindVertexArray(0);
	CHECK_GL(glBindTexture(GL_TEXTURE_CUBE_MAP, 0));
}
